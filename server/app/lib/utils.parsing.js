module.exports.parseLocationString = str => {
  let start, end, loc
  /*
   * Locs: Opéra Bastille, Palais Garnier, Amphithéâtre Bastille
   *
   * Formats date:
   * du 14 octobre au 16 novembre 2016
   * du 19 au 28 novembre 2016
   * du 10 septembre 2016 au 10 juin 2017
   * le 24 septembre 2016 à 19h30
   */



  return [start, end, loc]
}
